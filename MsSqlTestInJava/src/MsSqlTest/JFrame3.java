    /**
     * Handles the ActionPerformed event of the jButton2 control.
     * @param evt The ActionEvent instance containing the event data.
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Add
        try
        {
            // The fields must not be empty.
            if (name != null && !name.isEmpty())
            {
                id = 0;
                boolean matchFound = false;
                boolean deletedFound = false;

                // Open a connection to the database.
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection conn = DriverManager.getConnection(url, username, password);

                // Get the items from the Relationships table.
                Statement stmt = conn.createStatement();
                ResultSet rs = stmt.executeQuery("SELECT * FROM [MsSqlTestDatabase].[dbo].[Relationships]");
                
                // Get the number of the existing items.
                while (rs.next())
                {
                    id++;
                    
                    if (rs.getString("Name") == name && !matchFound && rs.getBoolean("IsDeleted") == false)
                    {
                        matchFound = true;
                        
                        break;
                    }
                    
                    if (rs.getString("Name") == name && !deletedFound && rs.getBoolean("IsDeleted") == true)
                    {
                        deletedFound = true;
                        
                        break;
                    }
                }

                // Close the resultSet.
                rs.close();
                
                if (!matchFound)
                {
                    if (!deletedFound)
                    {
                        // Add new item to the Relationships table.
                        PreparedStatement ps = conn.prepareStatement("INSERT INTO [MsSqlTestDatabase].[dbo].[Relationships] (Id, Name, IsDeleted) VALUES (?, ?, ?)");

                        // Set the values of the preparedStatement.
                        ps.setInt(1, id + 1);
                        ps.setString(2, name);
                        ps.setBoolean(6, false);

                        // Execute.
                        ps.execute();

                        // Close the preparedStatement.
                        ps.close();
                    }
                    else
                    {
                        // Edit the IsDeleted value to false.
                        PreparedStatement ps = conn.prepareStatement("UPDATE [MsSqlTestDatabase].[dbo].[Relationships] SET IsDeleted = ? WHERE Id = ?");

                        // Set the values of the preparedStatement.
                        ps.setBoolean(1, false);
                        ps.setInt(2, id);

                        // Update the table.
                        ps.executeUpdate();

                        // Close the preparedStatement.
                        ps.close();
                    }
                }

                // Close the connection.
                conn.close();

                // Refresh.
                GetData();
            }
        }
        catch (Exception e)
        {

        }
    }//GEN-LAST:event_jButton2ActionPerformed

