    /**
     * Gets the data.
     */
    private void GetData()
    {
        try
        {
            // Clear existing items.
            DefaultTableModel dm = (DefaultTableModel)jTable1.getModel();
            dm.getDataVector().removeAllElements();
            
            // Open a connection to the database.
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection conn = DriverManager.getConnection(url, username, password);
            
            // Get the items from the Relationships table.
            ArrayList<Relationship> relationships = new ArrayList<>();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM [MsSqlTestDatabase].[dbo].[Relationships] WHERE IsDeleted = 0");

            // Store the items in a Relationship list.
            while (rs.next())
            {
                Relationship relationship = new Relationship();
                
                relationship.setId(rs.getInt("Id"));
                relationship.setName(rs.getString("Name"));
                relationship.setIsDeleted(rs.getBoolean("IsDeleted"));
                
                relationships.add(relationship);
            }
            
            // Close the resultSet, the statement and the connection.
            rs.close();
            stmt.close();
            conn.close();
            
            // Fill the jTable rows with the values of the Relationships table.
            for (int i = 0; i < relationships.size(); i++)
            {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                
                model.addRow(new Object[]
                {
                    relationships.get(i).getId(),
                    relationships.get(i).getName()
                });
            }
        }
        catch (Exception e)
        {
            
        }
    }
    
